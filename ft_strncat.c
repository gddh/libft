#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t len;

	i = 0;
	len = ft_strlen(s1);
	while (i < n && s2[i] != '\0')
	{
		s1[len + i] = s2[i];
		i = i + 1;
	}
	s1[len + i] = '\0';
	return (s1);
}
