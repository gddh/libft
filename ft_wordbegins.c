#include "libft.h"

int		ft_wordbegins(char const *s, int i, char c)
{
	if (i == 0 && s[i] != c)
		return (1);
	if (s[i] != c && s[i - 1] == c && s[i] != '\0')
		return (1);
	return (0);
}
